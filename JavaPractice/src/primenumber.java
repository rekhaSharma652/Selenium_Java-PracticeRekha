
public class primenumber {
	public static void main(String args[])
	{
		int n=20;
		if(n==0 || n==1)
		{
			System.out.println("This is not prime number");
		}
		else if(n%2 == 0)
		{
			System.out.println("This is not prime number");
		}
		else
		{
			System.out.println("This is prime number");
		}
	}

}
